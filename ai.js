const { GoogleGenerativeAI } = require('@google/generative-ai');

/**
 * ฟังก์ชันตอบคำถามด้วย AI โดยใช้ข้อมูลจาก Sheets เป็นบริบท
 * ปรับปรุง: ใช้เวอร์ชัน v1 (Stable) เพื่อป้องกัน Error 404 ใน v1beta
 */
async function askAI(userQuestion, sheetContext) {
  if (!process.env.GEMINI_API_KEY) return "⚠️ ยังไม่ได้ตั้งค่า GEMINI_API_KEY ในระบบครับ";

  // ปรับชื่อโมเดลเป็นรุ่น Gemini 3/3.5 ที่มีสถานะใช้งานได้จริงในปัจจุบัน (ปี 2026) 
  // การใช้โมเดลสำรองที่มีโควตาแยกกันช่วยแก้ปัญหา 429 (Too Many Requests) และป้องกัน 404 ได้เป็นอย่างดี
  const modelNames = ['gemini-3.5-flash', 'gemini-3-flash', 'gemini-3.1-flash-lite'];
  const errors = [];

  for (const modelName of modelNames) {
    try {
      // โหลดโมเดล (ระบุ apiVersion เป็น v1 เพื่อความเสถียร)
      const model = genAI.getGenerativeModel({ model: modelName }, { apiVersion: 'v1' });
      
      const systemPrompt = `
        คุณคือ "ผู้ช่วยอัจฉริยะ สายตรวจภูธรลานสัก"
        หน้าที่ของคุณคือการช่วยเหลือและตอบคำถามเกี่ยวกับบุคลากร ผู้นำชุมชน และเบอร์โทรของ สภ.ลานสัก
        
        กฎการทำงานและความฉลาดในการจัดหมวดหมู่:
        1. การจัดกลุ่มตามฝ่ายงาน (สำคัญมาก):
           เมื่อผู้ใช้ขอรายชื่อแยกตามฝ่าย หรือตามหน้าที่ ให้กรองหาข้อมูลที่สอดคล้องกันในระบบดังนี้:
           - "จราจร" หรือ "จร" -> ค้นหาคำว่า จราจร หรือตำแหน่งที่ลงท้ายด้วย จร.
           - "ปราบปราม", "ป้องกันปราบปราม", "งานป้องกัน", "ป" -> ค้นหาคำว่า ป้องกันปราบปราม หรือสายตรวจ
           - "สืบสวน", "สส" -> ค้นหาคำว่า สืบสวน
           - "สอบสวน" -> ค้นหาคำว่า สอบสวน หรือตำแหน่งพนักงานสอบสวน
           - "อำนวยการ", "ธุรการ", "บริหาร" -> ค้นหาคำว่า อำนวยการ หรือธุรการ
           
        2. รูปแบบการนำเสนอข้อมูล:
           - หากผู้ใช้ขอรายชื่อตำรวจหรือหน่วยงาน ให้สรุปแยกเป็นข้อๆ อย่างชัดเจนและเป็นระเบียบ เช่น:
             ยศ ชื่อ-นามสกุล | ตำแหน่ง: xxx | ฝ่าย: xxx | เบอร์โทร: xxx (หากไม่มีเบอร์โทร ให้ใส่ "-")
           - แสดงข้อมูลให้กระชับ ครบถ้วน และอ่านง่ายทางโทรศัพท์มือถือ
           
        3. การตรวจสอบข้อมูล:
           - หากคำถามเกี่ยวข้องกับข้อมูลรายชื่อคน ตำแหน่ง เบอร์โทรศัพท์ หรือข้อมูลภายในระบบ ให้ตรวจสอบข้อมูลในระบบด้านล่างอย่างละเอียดก่อนตอบ
           - หากหาข้อมูลไม่พบในระบบจริงๆ ให้ตอบว่า "ไม่พบข้อมูลดังกล่าวในระบบครับ"
           
        4. การพูดคุยทั่วไป:
           - หากเป็นการทักทายทั่วไป หรือคำถามทั่วไปที่ไม่เกี่ยวข้องกับข้อมูลระบบ (เช่น ถามสารทุกข์สุกดิบ, ถามเวลา, คำนวณเลข, ทดสอบบอท):
             ให้ตอบสนองอย่างเป็นมิตร สุภาพ และมีไมตรีจิตในฐานะผู้ช่วยตำรวจลานสัก (ไม่ต้องตอบว่า "ไม่พบ")
        
        ข้อมูลในระบบ:
        ${sheetContext || 'ไม่มีข้อมูล'}
      `;

      const result = await model.generateContent([systemPrompt, userQuestion]);
      const response = await result.response;
      const text = response.text();
      
      if (text) return text;
    } catch (err) {
      console.error(`AI Error (${modelName}):`, err.message);
      errors.push(`${modelName}: ${err.message}`);
      continue;
    }
  }

  return `❌ AI ขัดข้อง:\n${errors.join('\n')}\n\n💡 กรุณาตรวจสอบสิทธิ์การใช้งานและการตั้งค่าคีย์ที่ Google AI Studio`;
}

module.exports = { askAI };
