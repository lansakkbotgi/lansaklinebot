/**
 * สร้าง GoogleGenerativeAI instance จาก API Key
 */
let genAI = null;
function getGenAI() {
  if (!genAI && process.env.GEMINI_API_KEY) {
    genAI = new GoogleGenerativeAI(process.env.GEMINI_API_KEY);
  }
  return genAI;
}

/**
 * ฟังก์ชันตอบคำถามด้วย AI โดยใช้ข้อมูลจาก Sheets เป็นบริบท
 */
async function askAI(userQuestion, sheetContext) {
  if (!process.env.GEMINI_API_KEY) {
    return "⚠️ ยังไม่ได้ตั้งค่า GEMINI_API_KEY ในระบบครับ";
  }

  const ai = getGenAI();
  if (!ai) {
    return "⚠️ ไม่สามารถเชื่อมต่อ Gemini AI ได้ กรุณาตรวจสอบ API Key";
  }

  // ── รายชื่อโมเดลที่พร้อมใช้งานจริง (เรียงจากดีที่สุดและมีสิทธิ์ใช้งานจริงในปี 2026) ──
  const modelNames = [
    'gemini-3.5-flash',          // รุ่นใหม่ล่าสุดและฉลาดที่สุด (ผ่านทดสอบแล้วว่ามีโควตา)
    'gemini-3-flash-preview',    // รุ่น 3.0 พรีวิว (ผ่านทดสอบแล้วว่ามีโควตา)
    'gemini-3.1-flash-lite',     // รุ่นประหยัดทรัพยากร (ผ่านทดสอบแล้วว่ามีโควตา)
    'gemini-flash-lite-latest'   // โมเดลสำรองทั่วไป (ผ่านทดสอบแล้วว่ามีโควตา)
  ];
  const errors = [];

  for (const modelName of modelNames) {
    try {
      const model = ai.getGenerativeModel(
        { model: modelName },
        { apiVersion: 'v1beta' }
      );

      const systemPrompt = buildSystemPrompt(sheetContext);
      const result = await model.generateContent([systemPrompt, userQuestion]);
      const response = await result.response;
      const text = response.text();

      if (text) {
        console.log(`✅ AI ตอบสำเร็จด้วยโมเดล: ${modelName}`);
        return text;
      }
    } catch (err) {
      console.error(`AI Error (${modelName}):`, err.message);
      errors.push(`${modelName}: ${err.message}`);
      continue;
    }
  }

  return `❌ AI ขัดข้อง:\n${errors.join('\n')}\n\n💡 กรุณาตรวจสอบสิทธิ์การใช้งานและการตั้งค่าคีย์ที่ Google AI Studio`;
}

