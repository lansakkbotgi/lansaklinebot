const { GoogleGenerativeAI } = require('@google/generative-ai');

// ============================================================
//  ai.js — ระบบ AI อัจฉริยะ สายตรวจภูธรลานสัก (Enhanced v3.1)
//  แก้ไข: ปรับเปลี่ยนโมเดลและลำดับการเรียกใช้ให้สอดคล้องกับ Quota ที่ใช้งานได้จริง
//        (ผ่านการทดสอบว่า gemini-3.5-flash, gemini-3.1-flash-lite มีสิทธิ์ใช้งาน)
